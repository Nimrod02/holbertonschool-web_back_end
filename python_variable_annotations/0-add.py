#!/usr/bin/env python3
"""module 0 - add
"""


def add(a: float, b: float) -> float:
    """a type-annotated function add
    that takes a float a and a float b as arguments
    and returns their sum as a float.

    Args:
        a (float): first number
        b (float): second number

    Returns:
        float: return the sum
    """
    return a + b
